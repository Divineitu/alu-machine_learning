#!/usr/bin/env python3
"""Module for concatenating two arrays"""

def cat_arrays(arr1, arr2):
    """Concatenates two arrays and returns a new list

    Args:
        arr1: First list of ints/floats
        arr2: Second list of ints/floats

    Returns:
        A new list containing all elements of arr1 followed by arr2
    """
    return arr1 + arr2
